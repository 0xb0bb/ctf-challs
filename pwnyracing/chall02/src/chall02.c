int read_input(char *input, long *size, long *debug){

    ssize_t n;
    if ((n = read(0, input, INPUT_SIZE)) == 0)
        return n;

    if (input[n-1] == '\n')
        input[n-1] = '\0';

    *size = strlen(input);
    if (*size == 0)
        return n;
    
    memset(cvals, '\0', sizeof(cvals));

    int i, v = 0;
    for (i = 0; i < *size; i += CHUNK_SIZE)
        cvals[v++] = crc32(0, (const char *) &input[i], MIN(*size-i, CHUNK_SIZE));

    if (*(long *) debug > 0)
        dump("\e[1mdebug:\e[0m         ", input, *size);

    return n;
}

